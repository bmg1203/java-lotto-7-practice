package lotto;

import java.util.List;
import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.*;
import static org.assertj.core.api.Assertions.*;

public class WinningNumberInputHandlerTest {
    @Test
    void shouldReturnStringArrayWhenSplitNumbers() {
        WinningNumberInputHandler winningNumberInputHandler = new WinningNumberInputHandler();
        assertThat(winningNumberInputHandler.splitNumbers("1,2,3,4,5,6"))
                .hasSize(6)
                .contains("1")
                .contains("2")
                .contains("3")
                .contains("4")
                .contains("5")
                .contains("6");
    }

    @Test
    void shouldReturnIntegerListWhenParseArray() {
        WinningNumberInputHandler winningNumberInputHandler = new WinningNumberInputHandler();
        assertThat(winningNumberInputHandler.stringInputParseInteger("1,2,3,4,5,6"))
                .hasSize(6)
                .contains(1)
                .contains(2)
                .contains(3)
                .contains(4)
                .contains(5)
                .contains(6);
    }
}
