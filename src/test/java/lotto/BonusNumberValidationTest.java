package lotto;

import lotto.view.ValidatorOfView;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;

import java.util.Arrays;
import java.util.List;

import static org.assertj.core.api.AssertionsForClassTypes.assertThatThrownBy;

public class BonusNumberValidationTest {
    @Test
    @DisplayName("보너스 번호와 로또 당첨 번호가 중복되면 예외 발생")
    void createBonusDuplicateOfWinningNumber() {
        List<Integer> winningNumbers = Arrays.asList(1, 2, 3, 4, 5, 6);
        assertThatThrownBy(() -> ValidatorOfView.isValidBonusNumber(winningNumbers,3))
                .isInstanceOf(IllegalArgumentException.class)
                .hasMessageContaining("[ERROR]");
    }
}
